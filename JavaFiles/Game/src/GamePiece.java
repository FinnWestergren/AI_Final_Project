
//used for Player interaction with board.
public class GamePiece {
	
	private int row, col;
	
	public GamePiece() {
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
	
	

}
